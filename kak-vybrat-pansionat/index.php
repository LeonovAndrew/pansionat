    <h2 class="how2__h2">Специализированный уход</h2>

    <p class="how2__p">Учитывая состояние здоровья пожилого, важно выбирать заведения со специализированным уходом, если
        есть такая необходимость. Для лежачих постояльцев и пожилых людей с ограниченными возможностями пансионаты
        должны быть оборудованы специальными пандусами. Они позволяют комфортно перемещать кресла-каталки. Пансионаты
        имеют в штате обученных и опытных специалистов, понимающих особенности разных заболеваний у пожилых людей и
        умеющих с ними корректно обращаться.</p>

