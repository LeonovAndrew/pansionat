    <h2 class="how2__h2">Является ли цена самым важным показателем?</h2>
    <p class="how2__p">
        Выбирая подходящий пансионат, большинство людей первым делом изучает стоимость проживания в сутки. Такой подход
        считается неправильным. Во многих пансионатах указывается в основном цена за «базовый пакет». При этом
        дополнительные услуги выбираются и оплачиваются с учетом заболеваний и состояния постояльца. Под каждого клиента
        в частном доме престарелых расписываются индивидуальные условия проживания. В госучреждениях подобного типа
        такой уровень комфорта недоступен. Здесь программа для постояльцев чаще всего шаблонна. При этом условия
        проживания, по сравнению с частными домами, будут намного хуже по уровню комфорта. Учитывая существенную разницу
        в финансировании таких учреждений, это логично.
    </p>

