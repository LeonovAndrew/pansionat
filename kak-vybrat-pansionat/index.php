    <h3 class="how2__h3">Цена проживания в частном пансионате часто зависит от:</h3>

    <ul class="how2__ul">
        <li>Срока пребывания постояльца (долговременного или краткосрочного);</li>
        <li>Питания (может быть 3-х, 4-х и пятиразовым);</li>
        <li>Наличия в штате учреждения узкоспециализированных врачей;</li>
        <li>Максимального количества проживающих в одной комнате;</li>
        <li>Необходимости оказания специального сестринского ухода.</li>
    </ul>

    <p class="how2__p">Несмотря на цену и условия проживания, в любом случае переезд для пожилого человека – это всегда
        стресс. Чтобы сделать его не таким травматичным для психики, важно подготовить пожилого родственника к переезду
        в новое место заранее.</p>

